 ## Logical Operators

 *Logical operators* modify or combine the Boolean logic values `true` and `false`. Swift supports the three standard logical operators found in C-based languages:

 * Logical NOT `(!a)`

 * Logical AND `(a && b)`

 * Logical OR `(a || b)`
