 ## Control Transfer Statements

 *Control transfer statements* change the order in which your code is executed, by transferring control from one piece of code to another. Swift has five control transfer statements:

 * continue

 * break

 * fallthrough

 * return

 * throw

 The `continue`, `break`, and `fallthrough` statements are described in the next few lessons of the course. The `return` statement is described in [Functions](https://docs.swift.org/swift-book/documentation/the-swift-programming-language/functions/), and the `throw` statement is described in [Propagating Errors Using Throwing Functions](https://docs.swift.org/swift-book/documentation/the-swift-programming-language/errorhandling/#Propagating-Errors-Using-Throwing-Functions)\.
